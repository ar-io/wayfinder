/**
 * Create an icon element with optional size and color
 * @param {string} iconName - Name of the icon from lucideIcons
 * @param {Object} options - Options for the icon
 * @param {number} options.size - Size in pixels (default: 16)
 * @param {string} options.class - CSS class to apply
 * @param {string} options.color - Color for the stroke
 * @returns {string} HTML string of the icon
 */
export function createIcon(iconName, options = {}) {
  const { size = 16, class: className = '', color = 'currentColor' } = options;
  const icon = lucideIcons[iconName];

  if (!icon) {
    logger.warn(`Icon "${iconName}" not found in lucideIcons`);
    return '';
  }

  // Replace width, height, and stroke color in the SVG
  return icon
    .replace(/width="\d+"/, `width="${size}"`)
    .replace(/height="\d+"/, `height="${size}"`)
    .replace(/stroke="[^"]*"/, `stroke="${color}"`)
    .replace('<svg', `<svg class="${className}"`);
}

/**
 * Create an inline icon for use in text
 * @param {string} iconName - Name of the icon
 * @returns {string} HTML string of the inline icon
 */
export function inlineIcon(iconName) {
  return createIcon(iconName, {
    size: 14,
    class: 'inline-icon',
  });
}

